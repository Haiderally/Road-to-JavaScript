const month = 'april';      //if we want to check for strings we use this syntax

switch (month) {
    case 'jan':
        console.log('jan')
        break;
    case 'feb':
        console.log('feb')
        break;
    case 'march':
        console.log('march')
        break;
    case 'april':
        console.log('april')
        break;
    case 'may':
        console.log('may')
        break;
    
    default:
        console.log(Default);
        break;
}